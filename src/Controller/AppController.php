namespace App\Controller;

use Cake\Controller\Controller;

/**
 * Application Controller
 *
 * Add your application-wide methods in the class below, your controllers
 * will inherit them.
 *
 * @link https://book.cakephp.org/4/en/controllers.html#the-app-controller
 */
class AppController extends Controller
{

/* Start Authentication */
/* Authentication tutorial */
public function beforeFilter(\Cake\Event\EventInterface $event)
{
    parent::beforeFilter($event);
    // actions public, skipping the authentication check for the following actions [uncomment if have issue with insertion of a valid user]
    // $this->Authentication->addUnauthenticatedActions(['index', 'view', 'add']);
    $this->Authentication->addUnauthenticatedActions(['display']); // Allows viewing of files under the templates/pages folder [so these are made public end]
}
/* End Authentication */

    /**
     * Initialization hook method.
     *
     * Use this method to add common initialization code like loading components.
     *
     * e.g. `$this->loadComponent('FormProtection');`
     *
     * @return void
     */
    public function initialize(): void
    {
        parent::initialize();

        $this->loadComponent('RequestHandler');
        $this->loadComponent('Flash');

        /*
         * Enable the following component for recommended CakePHP form protection settings.
         * see https://book.cakephp.org/4/en/controllers/components/form-protection.html
         */
        //$this->loadComponent('FormProtection');

        /* Start Authentication */
            $this->loadComponent('Authentication.Authentication');
        /* End Authentication */
        /* Start Authorisation */
        $this->loadComponent('Authorization.Authorization');
        /* End Authorisation */

    }
}

